package dev.osakareaper;

public class Main {
    public static void main(String[] args) {

        // Declaring Variables
        // [type] [identifier] = [data];

        // Integers
        int brainSize = 99999;
        System.out.println("My brain size: " + brainSize);

        // Floating points
        double testScore = 9.5;
        System.out.println("My 1st test score: " + testScore);

        float testScore2 = 8.7F;
        System.out.println("My 2nd test score: " + testScore2);

        // String
        String toPuto = "DOESN'T EVEN MAKE SENSE WTFFFFF";
        System.out.println(toPuto);

    }
}
